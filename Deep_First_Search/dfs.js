function drawGrid() {
    ctx.strokeStyle = "black";

    for(var i = 0.5; i < 34; i++) {
        for(var j = 0.5; j < 35; j++) {
            var x = i * 15;
            var y = j * 15;
            ctx.beginPath();
            ctx.rect(x,y,15,15);
            ctx.stroke();
        }
    }
}

async function test() {
    ctx.strokeStyle = "black";

    for(var i = 0.5; i < 36; i++) {
        for(var j = 0.5; j < 35; j++) {
            if(grid[j-0.5][i-0.5] === '0') {
                setWall(i,j);
                await sleep(20);
            } else if(grid[j-0.5][i-0.5] === 's') {
                setStart(i,j);
                await sleep(20);
            } else if(grid[j-0.5][i-0.5] === 'e') {
                setTarget(i,j);
                await sleep(20);
            }
        }
    }
}

function setWall(x,y) {
    ctx.beginPath();
    ctx.rect(x*15,y*15,15,15);
    ctx.fillStyle = "red";
    ctx.fill();
    ctx.stroke();
}

function setStart(x,y) {
    ctx.beginPath();
    ctx.rect(x*15,y*15,15,15);
    ctx.fillStyle = "green";
    ctx.fill();
    ctx.stroke();
}

function setTarget(x,y) {
    ctx.beginPath();
    ctx.rect(x*15,y*15,15,15);
    ctx.fillStyle = "blue";
    ctx.fill();
    ctx.stroke();
}

function sleep(millis) {
    return new Promise(resolve => setTimeout(resolve,millis));
}